# 导入数值计算库NumPy，用于矩阵运算、随机数生成等
import numpy as np
# 导入matplotlib的pyplot模块，用于绘制直方图可视化激活值分布
import matplotlib.pyplot as plt


# 定义sigmoid激活函数
# 作用：将任意实数压缩到 0~1 之间，常用于二分类输出层
# 输入：x - 数值、向量或矩阵（支持NumPy广播）
# 输出：对应位置的sigmoid值，值域(0,1)
def sigmoid(x):
    return 1 / (1 + np.exp(-x))  # sigmoid标准公式：1/(1+e^{-x})


# 定义ReLU激活函数
# 作用：负数输出0，正数输出原值，是深度学习最常用的隐藏层激活函数
# 优点：计算简单，缓解梯度消失，收敛快
def ReLU(x):
    return np.maximum(0, x)  # 取0和x中的较大值，实现单侧抑制


# 定义tanh激活函数
# 作用：将任意实数压缩到 -1~1 之间，中心化效果比sigmoid好，输出均值为0附近
# 适用于需要进行零中心化处理的特征
def tanh(x):
    return np.tanh(x)  # NumPy自带的tanh函数，内部实现双曲正切


# ===================== 1. 生成输入数据 =====================
# 生成输入数据：1000个样本，每个样本100个特征
# np.random.randn：生成服从标准正态分布（均值0，方差1）的随机数，模拟经过预处理的特征
input_data = np.random.randn(1000, 100)  # 形状(1000, 100)

node_num = 100  # 每个隐藏层的神经元（节点）数量，保持维度不变
hidden_layer_size = 5  # 神经网络一共设置5层隐藏层
activations = {}  # 字典：用来保存每一层隐藏层的激活输出结果，键为层索引(0~4)，值为激活矩阵

# ===================== 2. 前向传播（核心实验部分） =====================
x = input_data  # 第一层的输入 = 最开始的输入数据，形状(1000,100)

# 循环遍历5层隐藏层，逐层计算激活值，模拟前向传播过程
for i in range(hidden_layer_size):
    # 除了第一层，后续层的输入 = 上一层的激活输出
    if i != 0:
        x = activations[i - 1]  # 取上一层的激活输出作为当前层输入

    # ===================== 权重初始化（实验核心：修改这里观察激活值分布） =====================
    # 权重矩阵形状：(当前层节点数, 下一层节点数) = (100,100)
    # np.random.randn：正态分布随机初始化权重
    # 不同初始化系数直接影响加权和的方差，进而影响激活值的分布

    # 方案1：权重标准差=1（激活值会饱和，梯度消失）
    # 由于输入方差1，权重方差1，加权和方差≈node_num，传给sigmoid后大部分值趋近0或1，梯度很小
    w = np.random.randn(node_num, node_num) * 1

    # 方案2：权重标准差=0.01（激活值集中在0.5附近，表达能力差）
    # 加权和方差过小，sigmoid输出集中在0.5附近，输出分布过于集中，不利于特征区分
    # w = np.random.randn(node_num, node_num) * 0.01

    # 方案3：Xavier初始化（适合sigmoid/tanh激活函数）
    # 使各层激活值和梯度方差尽量一致，权重方差设为1/node_num（输入节点数）
    # w = np.random.randn(node_num, node_num) * np.sqrt(1.0 / node_num)

    # 方案4：He初始化（适合ReLU激活函数）
    # 考虑ReLU的负半轴截断，权重方差设为2/node_num，保持方差稳定
    # w = np.random.randn(node_num, node_num) * np.sqrt(2.0 / node_num)

    # 计算加权和：输入矩阵x与权重矩阵w点乘，形状仍为(1000,100)
    a = np.dot(x, w)

    # ===================== 激活函数（实验核心：切换观察分布） =====================
    # 当前使用sigmoid进行演示，可替换为ReLU或tanh观察不同激活函数下分布的变化
    z = sigmoid(a)  # 方案1：sigmoid激活，输出范围(0,1)
    # z = ReLU(a)        # 方案2：ReLU激活，输出范围[0, +∞)，实际操作中大约一半为0
    # z = tanh(a)        # 方案3：tanh激活，输出范围(-1,1)，具有零中心化特性

    # 把当前层的激活结果存入字典，键为层索引i，值为激活矩阵z
    activations[i] = z

# ===================== 3. 绘制激活值分布直方图 =====================
# 遍历每一层的激活值，画子图展示分布，直观对比各层激活值的集中/饱和情况
for i, a in activations.items():
    # 创建1行多列子图，选中第i+1个子图
    plt.subplot(1, len(activations), i + 1)
    # 子图标题：第1层、第2层...第5层
    plt.title(str(i + 1) + "-layer")

    # 除了第一个子图，其他子图不显示y轴刻度，保持画面简洁
    if i != 0:
        plt.yticks([], [])

    # 绘制直方图：
    # a.flatten()：把多维激活值展平成一维，便于统计
    # 30：柱子数量，控制直方图细分粒度
    # range=(0,1)：x轴范围0~1，与sigmoid输出范围对应（若换其他激活函数需调整范围）
    plt.hist(a.flatten(), 30, range=(0, 1))

# 显示所有子图，便于对比分析不同层激活值的分布变化趋势
plt.show()