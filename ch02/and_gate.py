# coding: utf-8
# 导入 numpy 库，主要用于数组的向量化运算
import numpy as np


def AND(x1, x2):
    """
    实现与门（AND gate）的逻辑函数。
    使用简单感知机模型：y = 1 当且仅当 x1 和 x2 都为 1，
    否则 y = 0。
    感知机的决策规则：若 w·x + b > 0 则输出 1，否则输出 0。
    """
    # 将输入信号组合成一个包含两个元素的特征向量
    x = np.array([x1, x2])
    # 权重向量：两个输入对结果的贡献相同，各占 0.5
    w = np.array([0.5, 0.5])
    # 偏置值，用于移动决策边界；负值使得只有当两个输入都为 1 时加权和才超过阈值
    b = -0.7
    # 计算加权和并加上偏置：tmp = w1*x1 + w2*x2 + b
    tmp = np.sum(w * x) + b
    # 根据加权和是否大于 0 来输出分类结果
    if tmp <= 0:
        return 0
    else:
        return 1


# 程序入口：当脚本直接运行时执行以下测试代码
if __name__ == '__main__':
    # 遍历所有可能的二进制输入组合，验证与门的功能
    for xs in [(0, 0), (1, 0), (0, 1), (1, 1)]:
        # 调用 AND 函数计算当前输入组合下的输出
        y = AND(xs[0], xs[1])
        # 打印输入组合及其对应的与门输出
        print(str(xs) + " -> " + str(y))