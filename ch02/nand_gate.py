# 导入 numpy 科学计算库，用于数组运算
import numpy as np


# 实现与非门（NAND gate）的前向计算
# 输入两个信号 x1 和 x2，输出一个二值信号 0 或 1
def NAND(x1, x2):
    # 将两个输入组合成 numpy 数组，方便进行向量化运算
    x = np.array([x1, x2])
    # 权重向量，分别对应 x1 和 x2 的权重，均为 -0.5
    w = np.array([-0.5, -0.5])
    # 偏置项，用于调整神经元激活的阈值
    b = 0.7
    # 计算加权和并加上偏置：w1*x1 + w2*x2 + b
    tmp = np.sum(w * x) + b
    # 如果加权和小于等于 0，则输出 0（表示非激活状态）
    if tmp <= 0:
        return 0
    # 否则输出 1（表示激活状态）
    else:
        return 1

# 如果当前脚本作为主程序运行，则执行以下测试代码
if __name__ == '__main__':
    # 遍历所有可能的输入组合：(0,0), (1,0), (0,1), (1,1)
    for xs in [(0, 0), (1, 0), (0, 1), (1, 1)]:
        # 调用 NAND 函数得到输出
        y = NAND(xs[0], xs[1])
        # 打印输入组合及其对应的输出结果
        print(str(xs) + " -> " + str(y))