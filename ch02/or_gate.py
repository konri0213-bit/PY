import numpy as np  # 导入 NumPy 数值计算库，并简写为 np，便于进行向量化运算

# 用简单的感知机模型实现逻辑“或”（OR）门
def OR(x1, x2):
    """
    接收两个二进制输入 x1 和 x2（取值为 0 或 1），
    返回它们的逻辑“或”结果。
    该函数通过权重与偏置的线性组合实现感知机。
    """
    # 将两个输入组合成一个 NumPy 数组，便于后续向量化计算
    x = np.array([x1, x2])
    # 定义权重向量：两个输入的贡献相同，各为 0.5
    w = np.array([0.5, 0.5])
    # 偏置项 b = -0.2，用于调整感知机的激活阈值
    b = -0.2
    # 计算加权和并加上偏置 (w1*x1 + w2*x2 + b)
    tmp = np.sum(w * x) + b
    # 应用阶跃激活函数：若线性组合结果 <= 0 则输出 0，否则输出 1
    if tmp <= 0:
        return 0
    else:
        return 1

# 当脚本作为主程序直接运行时，执行以下测试代码
if __name__ == '__main__':
    # 遍历所有可能的输入组合 (0,0), (1,0), (0,1), (1,1)
    for xs in [(0, 0), (1, 0), (0, 1), (1, 1)]:
        # 调用 OR 函数计算当前输入组合的逻辑“或”结果
        y = OR(xs[0], xs[1])
        # 将输入元组转换为字符串，与其运算结果一并打印，例如 (0, 1) -> 1
        print(str(xs) + " -> " + str(y))